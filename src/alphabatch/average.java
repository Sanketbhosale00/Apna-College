package alphabatch;
import java.util.*;
public class average {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		double A=sc.nextDouble();
		double B=sc.nextDouble();
		double C=sc.nextDouble();
		double avg=(A+B+C)/3;
		System.out.println("Average of A,B&C is: "+avg);
	}

}
